def movement(fruits_places, p_x, p_y, score):

    while True:
        move_rotation = input("Choose your move wisely : 'W' for up | 'S' for down | 'A' for left | 'D' for right\n")
        while (is_valid(move_rotation, "move_inp") == 1):
                move_rotation = input("Choose your move wisely : 'W' for up | 'S' for down | 'A' for left | 'D' for right\n")
        if (move_rotation == 'W' or move_rotation == 'w'):
            temp = p_x - 1
            if (temp < 10 and temp >= 0):
                p_x -= 1
            else:
                return 1
            if (p_x, p_y) in (fruits_places):
                score += 10
                fruits_places.remove((p_x, p_y))
                print("\033[0;32mCONGRATULATIONS ! YOU EARNED +10 POINTS !\033[0;32m" + "\033[0m")
            labyrent_generator(p_x, p_y, fruits_places, score)
        elif (move_rotation == 'S' or move_rotation == 's'):
            temp = p_x + 1
            if (temp < 10 and temp >= 0):
                p_x += 1
            else:
                return 1
            if (p_x, p_y) in (fruits_places):
                score += 10
                fruits_places.remove((p_x, p_y))
                print("\033[0;32mCONGRATULATIONS ! YOU EARNED +10 POINTS !\033[0;32m" + "\033[0m")
            labyrent_generator(p_x, p_y, fruits_places, score)
        elif (move_rotation == 'A' or move_rotation == 'a'):
            temp = p_y - 1
            if (temp < 10 and temp >= 0):
                p_y -= 1
            else:
                return 1
            if (p_x, p_y) in (fruits_places):
                score += 10
                fruits_places.remove((p_x, p_y))
