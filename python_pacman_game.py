def is_valid(arg, type):

    control = 0
    if (type == "fruit_count"):
        try:
            arg == int(arg)
        except ValueError:
            print("Invalid input : Please enter a number !\n")
            control = 1
        if (control == 0):
            arg = int(arg)
            if (arg <= 0):
                print("Invalid input : Fruit number must be higher than zero !\n")
                control = 1
            elif (arg >= 100):
                print("Invalid input : Fruit number must not be higher than 99 !\n")
                control = 1
    elif (type == "move_inp"):
            if ((arg != 'W' and arg != 'S' and arg != 'A' and arg != 'D') and  (arg != 'w' and arg != 's' and arg != 'a' and arg != 'd')):
                print("Invalid input : 'W' for up | 'S' for down | 'A' for left | 'D' for right\n")
                control = 1
    return control

