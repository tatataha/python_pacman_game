def fruit_place_generator(fruit_number):
    
    fruit_place_list = set()
    
    while (len(fruit_place_list) < fruit_number):
        x = random.randint(0, 9)
        y = random.randint(0, 9)
        fruit_place_list.add((x, y))
    
    return fruit_place_list

